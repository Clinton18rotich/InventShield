import React, { useState } from 'react'
import countries from "./countries.js"

function NDAGenerator({ dark }) {
  const [form, setForm] = useState({ discloser:'', recipient:'', invention:'', country:'KE', date:new Date().toLocaleDateString() })
  const [generated, setGenerated] = useState(false)
  const bg=dark?'#111827':'#f9fafb'; const cb=dark?'#1f2937':'white'; const txt=dark?'#f9fafb':'#111827'
  const country = countries.find(c=>c.code===form.country)
  
  const generate = () => {
    if(!form.discloser||!form.recipient||!form.invention) return
    setGenerated(true)
  }
  
  const download = () => {
    const nda = `NON-DISCLOSURE AGREEMENT\n\nThis NDA is made on ${form.date}\n\nBETWEEN:\nDisclosing Party: ${form.discloser}\nReceiving Party: ${form.recipient}\n\nINVENTION: ${form.invention}\n\nTERMS:\n1. The Receiving Party agrees to keep all information confidential.\n2. Confidential information includes all technical, business, and financial details.\n3. This obligation survives for 5 years from disclosure date.\n4. Breach entitles the Disclosing Party to seek injunctive relief and damages.\n5. This Agreement shall be governed by the ${country.law}.\n6. Any disputes shall be resolved through arbitration in ${country.name}.\n\nIP OFFICE: ${country.ipOffice}\n\nSIGNATURES:\n_____________________    _____________________\nDisclosing Party           Receiving Party\n\nGenerated by InventShield Legal Hub | inventShield.app/legal`
    const blob = new Blob([nda], {type:'text/plain'})
    const a = document.createElement('a')
    a.href = URL.createObjectURL(blob)
    a.download = 'NDA_' + form.invention.replace(/\s/g,'_') + '_' + form.country + '.txt'
    a.click()
  }
  
  const s = {width:'100%',padding:10,borderRadius:8,border:'1px solid '+(dark?'#374151':'#d1d5db'),marginBottom:8,background:cb,color:txt,fontSize:13,boxSizing:'border-box'}
  
  return <div style={{background:cb,borderRadius:14,padding:20,marginBottom:16}}>
    <h3 style={{fontSize:16,fontWeight:'bold',color:txt,marginBottom:12}}>📄 NDA Generator — International</h3>
    <p style={{fontSize:11,color:dark?'#9ca3af':'#6b7280',marginBottom:12}}>Generate legally-binding NDAs under your country&apos;s laws</p>
    {!generated ? <>
      <input placeholder="Your name (Disclosing Party)" value={form.discloser} onChange={e=>setForm({...form,discloser:e.target.value})} style={s} />
      <input placeholder="Recipient name" value={form.recipient} onChange={e=>setForm({...form,recipient:e.target.value})} style={s} />
      <input placeholder="Invention title" value={form.invention} onChange={e=>setForm({...form,invention:e.target.value})} style={s} />
      <select value={form.country} onChange={e=>setForm({...form,country:e.target.value})} style={s}>
        {countries.map(c=><option key={c.code} value={c.code}>{c.name} — {c.ipOffice}</option>)}
      </select>
      <button onClick={generate} disabled={!form.discloser||!form.recipient||!form.invention} style={{width:'100%',padding:12,borderRadius:8,background:(!form.discloser||!form.recipient||!form.invention)?'#d1d5db':'#4f46e5',color:'white',border:'none',cursor:(!form.discloser||!form.recipient||!form.invention)?'default':'pointer',fontWeight:600}}>Generate NDA</button>
    </> : <div style={{textAlign:'center'}}>
      <span style={{fontSize:40,display:'block',marginBottom:8}}>✅</span>
      <p style={{fontWeight:'bold',color:'#15803d',marginBottom:4}}>NDA Generated!</p>
      <p style={{fontSize:12,color:'#6b7280',marginBottom:4}}>{form.discloser} ↔ {form.recipient}</p>
      <p style={{fontSize:11,color:'#6b7280',marginBottom:4}}>📜 Governed by: {country.law}</p>
      <p style={{fontSize:11,color:'#6b7280',marginBottom:12}}>🏛️ IP Office: {country.ipOffice}</p>
      <button onClick={download} style={{width:'100%',padding:12,borderRadius:8,background:'#16a34a',color:'white',border:'none',cursor:'pointer',fontWeight:600,marginBottom:6}}>📥 Download NDA</button>
      <button onClick={()=>setGenerated(false)} style={{width:'100%',padding:10,borderRadius:8,background:'#e5e7eb',border:'none',cursor:'pointer',fontSize:12}}>Create Another</button>
    </div>}
  </div>
}

