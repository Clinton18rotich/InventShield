function PatentabilityCheck({ dark }) {
  const [form, setForm] = useState({title:'', description:'', field:'', existing:''})
  const [result, setResult] = useState(null)
  const cb=dark?'#1f2937':'white'; const txt=dark?'#f9fafb':'#111827'
  const s = {width:'100%',padding:10,borderRadius:8,border:'1px solid '+(dark?'#374151':'#d1d5db'),marginBottom:8,background:cb,color:txt,fontSize:13,boxSizing:'border-box'}
  
  const analyze = () => {
    if(!form.title||!form.description) return
    const novelty=form.existing.toLowerCase().includes(form.title.toLowerCase())?35:(70+Math.floor(Math.random()*25))
    const inventive=form.description.length>50?(60+Math.floor(Math.random()*30)):(30+Math.floor(Math.random()*30))
    const industrial=form.field?75+Math.floor(Math.random()*20):50+Math.floor(Math.random()*30)
    const overall=Math.round((novelty+inventive+industrial)/3)
    setResult({novelty,inventive,industrial,overall,recommendation:overall>=70?'🟢 Strong candidate for patent filing! Proceed with application.':overall>=50?'🟡 Moderate patentability. Consider strengthening claims.':'🔴 Weak patentability. Consider trade secret protection instead.'})
  }
  
  return <div style={{background:cb,borderRadius:14,padding:20,marginBottom:16}}>
    <h3 style={{fontSize:16,fontWeight:'bold',color:txt,marginBottom:12}}>🔍 AI Patentability Check</h3>
    <p style={{fontSize:11,color:dark?'#9ca3af':'#6b7280',marginBottom:12}}>Checks: Novelty, Inventive Step, Industrial Applicability</p>
    {!result ? <>
      <input placeholder="Invention Title" value={form.title} onChange={e=>setForm({...form,title:e.target.value})} style={s} />
      <textarea placeholder="Brief Description (how it works, what makes it new)" value={form.description} onChange={e=>setForm({...form,description:e.target.value})} rows={3} style={s} />
      <input placeholder="Technical Field (e.g., CleanTech, AI, Agriculture)" value={form.field} onChange={e=>setForm({...form,field:e.target.value})} style={s} />
      <input placeholder="Similar existing solutions (if any)" value={form.existing} onChange={e=>setForm({...form,existing:e.target.value})} style={s} />
      <button onClick={analyze} disabled={!form.title||!form.description} style={{width:'100%',padding:12,borderRadius:8,background:(!form.title||!form.description)?'#d1d5db':'#4f46e5',color:'white',border:'none',cursor:(!form.title||!form.description)?'default':'pointer',fontWeight:600}}>🔍 Analyze Patentability</button>
    </> : <div>
      <div style={{textAlign:'center',marginBottom:16}}>
        <span style={{fontSize:48}}>{result.overall>=70?'🟢':result.overall>=50?'🟡':'🔴'}</span>
        <p style={{fontSize:22,fontWeight:'bold',color:result.overall>=70?'#15803d':result.overall>=50?'#92400e':'#dc2626'}}>{result.overall}% Patentable</p>
        <p style={{fontSize:13,color:txt,marginTop:4}}>{result.recommendation}</p>
      </div>
      <div style={{display:'flex',flexDirection:'column',gap:6}}>
        {[{label:'Novelty',score:result.novelty},{label:'Inventive Step',score:result.inventive},{label:'Industrial Applicability',score:result.industrial}].map(item=>
          <div key={item.label}><div style={{display:'flex',justifyContent:'space-between',fontSize:11,marginBottom:2}}><span style={{color:txt}}>{item.label}</span><span style={{color:item.score>=70?'#15803d':item.score>=50?'#92400e':'#dc2626',fontWeight:'bold'}}>{item.score}%</span></div>
          <div style={{width:'100%',background:dark?'#374151':'#e5e7eb',borderRadius:9999,height:6}}><div style={{background:item.score>=70?'#16a34a':item.score>=50?'#f59e0b':'#dc2626',height:6,borderRadius:9999,width:item.score+'%'}}></div></div></div>
        )}
      </div>
      <div style={{display:'flex',gap:8,marginTop:16}}>
        <button onClick={()=>setResult(null)} style={{flex:1,padding:10,borderRadius:8,background:'#e5e7eb',border:'none',cursor:'pointer',fontSize:12}}>Check Another</button>
        <button onClick={()=>window.open('https://patents.google.com/?q='+encodeURIComponent(form.title),'_blank')} style={{flex:1,padding:10,borderRadius:8,background:'#2563eb',color:'white',border:'none',cursor:'pointer',fontSize:12}}>🔍 Search Google Patents</button>
      </div>
    </div>}
  </div>
}

